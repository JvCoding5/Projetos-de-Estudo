# Tipos int e float
# int -> numero inteiro
# O tipo int representa qualquer número
# positivo ou negativo, sem casas decimais.
# int sem sinal é considerado positivo.

print(11) #int
print(-11) #int
print(0) #int

# Float -> Número com ponto flutuante
# O tipo float representa números com casas decimais.
# positivo ou negativo, com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1) #float
print(-1.1) #float
print(0.0) #float

# A função type mostra o tipo de dado de uma variável.
print("O número 11 é do tipo:", type(11)) # <class 'int'>
print("O número -11 é do tipo:", type(-'')) # <class 'int'>


